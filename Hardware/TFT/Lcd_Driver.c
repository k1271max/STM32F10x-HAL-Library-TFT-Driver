void LCD_GPIO_Init(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;

	__HAL_RCC_GPIOB_CLK_ENABLE();
	__HAL_RCC_GPIOA_CLK_ENABLE();

	GPIO_InitStructure.Pin = LCD_RST | LCD_LED | LCD_RS;
	GPIO_InitStructure.Mode = GPIO_MODE_OUTPUT_PP;
	GPIO_InitStructure.Pull = GPIO_NOPULL;
	GPIO_InitStructure.Speed = GPIO_SPEED_FREQ_HIGH;
	HAL_GPIO_Init(GPIOB, &GPIO_InitStructure);

	GPIO_InitStructure.Pin = LCD_CS | LCD_SCL | LCD_SDA;
	HAL_GPIO_Init(GPIOB, &GPIO_InitStructure);


	LCD_CS_SET;
	LCD_SCL_SET;
	LCD_SDA_SET;
	LCD_RST_SET;
	LCD_LED_SET;
}

void  SPI_WriteData(u8 Data)
{
	GPIO_TypeDef *port = LCD_CTRLA;
	uint32_t sda = (uint32_t)LCD_SDA;
	uint32_t scl = (uint32_t)LCD_SCL;
	for (int i = 0; i < 8; i++)
	{
		if (Data & 0x80)
			port->BSRR = sda;
		else
			port->BRR = sda;

		port->BRR = scl; // SCL low
		port->BSRR = scl; // SCL high

		Data <<= 1;
	}
}


void Lcd_WriteIndex(u8 Index)
{

   LCD_CS_CLR;
   LCD_RS_CLR;
   SPI_WriteData(Index);
   LCD_CS_SET;
}

void Lcd_WriteData(u8 Data)
{
   LCD_CS_CLR;
   LCD_RS_SET;
   SPI_WriteData(Data);
   LCD_CS_SET; 
}

void LCD_WriteData_16Bit(u16 Data)
{
   LCD_CS_CLR;
   LCD_RS_SET;
	 SPI_WriteData(Data>>8); 
	 SPI_WriteData(Data); 		
   LCD_CS_SET; 
}

void Lcd_WriteReg(u8 Index,u8 Data)
{
	Lcd_WriteIndex(Index);
  Lcd_WriteData(Data);
}

void Lcd_Reset(void)
{
	LCD_RST_CLR;
	HAL_Delay(100);
	LCD_RST_SET;
	HAL_Delay(50);
}


